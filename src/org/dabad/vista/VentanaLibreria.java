    private void botonAgregarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarLibroActionPerformed
        Libro libro = new Libro();
        libro.setIdLibro(Integer.parseInt(txtIdLibro.getText()));
        libro.setNombreDelAutor(txtAutorLibro.getText());
        libro.setNombreDelLibro(txtNombreLibro.getText());
        libro.setNombreDeLaEditorial(txtEditorialLibro.getText());
        libro.setEdicion(Integer.parseInt(txtEdicionLibro.getText()));
        libro.setEstado(true);
        ControlDeLibro controlador = new ControlDeLibro();
        controlador.agregarLibro(libro);
        
        
    }//GEN-LAST:event_botonAgregarLibroActionPerformed

